    private void tblMedicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMedicosMouseClicked
        //para obtener siempre el valor del id seteo la columna siempre en 0 y el valor
        //del renglon lo obtengo en base al evento de clickear
        int renglon = tblMedicos.rowAtPoint(evt.getPoint());
        int columna = 0;
        int id = Integer.parseInt(tblMedicos.getValueAt(renglon, columna).toString());
        Medico medico = controlador.obtenerMedico(id);
        txtId.setText(String.valueOf(id));
        txtNombre.setText(medico.getNombre());
        txtApellido.setText(medico.getApellido());
        txtMatricula.setText(medico.getMatricula());
        cboEspecialidad.setSelectedIndex(medico.getEspecialidad().getId() - 1);
        txtDni.setText(medico.getDni());
    }//GEN-LAST:event_tblMedicosMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Esta seguro que quiere eliminar el registro?", "¡CUIDADO!", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            controlador.eliminarMedico(Integer.parseInt(txtId.getText()));
             
        }
        llenarTabla();
        limpiarCampos();
       

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        int id = Integer.parseInt(txtId.getText());
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String matricula = txtMatricula.getText();
        Especialidad especialidad = (Especialidad) cboEspecialidad.getSelectedItem();
        String dni = txtDni.getText();
        Medico medico = new Medico(id, nombre, apellido, matricula, especialidad, dni);
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Esta seguro que quiere actualizar el registro?", "¡CUIDADO!", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            controlador.actualizarMedico(medico);
            limpiarCampos();
        }
        llenarTabla();
        


    }//GEN-LAST:event_btnActualizarActionPerformed

