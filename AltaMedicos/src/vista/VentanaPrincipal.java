    private void llenarTabla() {
        ArrayList<Medico> lista = controlador.obtenerMedicos();
        DefaultTableModel tabla = new DefaultTableModel();
        String cabeceraTabla[] = new String[]{"ID", "NOMBRE", "APELLIDO", "MATRICULA", "ESPECIALIDAD", "DNI"};
        tabla.setColumnIdentifiers(cabeceraTabla);
        tblMedicos.setModel(tabla);
        for (Medico m : lista) {
            tabla.addRow(new Object[]{m.getId(), m.getNombre(), m.getApellido(), m.getMatricula(), m.getEspecialidad().getDescripcion(), m.getDni()});
        }

    }

    private void llenarCombo() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        ArrayList<Especialidad> lista = controlador.obtenerEspecialidades();

        for (Especialidad e : lista) {
            model.addElement(e);
        }

        cboEspecialidad.setModel(model);
    }

    private void limpiarCampos() {
        txtId.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        cboEspecialidad.setSelectedIndex(0);
        txtMatricula.setText("");
        txtDni.setText("");
    }

